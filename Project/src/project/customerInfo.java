    private void accountShowBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_accountShowBtnActionPerformed
        // Check if the username exists in savings_account table
        boolean isInSavingsAccount = checkUsernameInTable("savings_account", username);

        // Check if the username exists in current_account table
        boolean isInCurrentAccount = checkUsernameInTable("current_account", username);

        // Update lastAccessDate based on the presence in each table
        if (isInSavingsAccount) {
            updateLastAccessDate("savings_account_customer");
        }

        if (isInCurrentAccount) {
            updateLastAccessDate("current_account_customer");
        }
        this.dispose();
        new account(username).setVisible(true);
    }//GEN-LAST:event_accountShowBtnActionPerformed

    private boolean checkUsernameInTable(String tableName, String username) {
        try {
            String query = "SELECT COUNT(*) FROM " + tableName + " WHERE account_number = ?";
            st = con.prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();

            if (rs.next()) {
                int count = rs.getInt(1);
                return count > 0;
            }
        } catch (SQLException ex) {
            Logger.getLogger(customerInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

