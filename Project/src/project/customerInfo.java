    public void setCustomerInfo() {
        PreparedStatement st1 = null;
        ResultSet rs1 = null;
        try {
            // Truy vấn đầu tiên
            String query = "SELECT ssn, credit_rating, name, dob, street, city, province FROM customer WHERE customer_id=?";
            st = con.prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();

            if (rs.next()) {
                String ssntam = rs.getString("ssn");

                // Truy vấn thứ hai
                String query1 = "SELECT phone FROM person_phone WHERE ssn=?";
                st1 = con.prepareStatement(query1);
                st1.setString(1, ssntam);
                rs1 = st1.executeQuery();

                if (rs1.next()) {
                    phoneField.setText("0" + rs1.getString("phone"));
                }

                // Set các giá trị còn lại
                getCusID.setText(username);
                getSSN.setText(ssntam);
                getCredit.setText(rs.getString("credit_rating"));
                nameField.setText(rs.getString("name"));
                dobField.setText(rs.getString("dob"));
                streetField.setText(rs.getString("street"));
                cityField.setText(rs.getString("city"));
                provinceField.setText(rs.getString("province"));
            } else {
                JOptionPane.showMessageDialog(this, "Customer not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(customerInfo.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (st != null) {
                    st.close();
                }
                if (rs1 != null) {
                    rs1.close();
                }
                if (st1 != null) {
                    st1.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(customerInfo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

