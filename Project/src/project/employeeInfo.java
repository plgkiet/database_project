    public void setEmployeeInfo() {
        PreparedStatement st1 = null;
        ResultSet rs1 = null;
        try {
            // Truy vấn đầu tiên
            String query = "SELECT ssn, salary, join_date, manager_id, customer_id, name, dob, street, city, province FROM employee  WHERE employee_id=?";
            st = con.prepareStatement(query);
            st.setString(1, username);
            rs = st.executeQuery();

            if (rs.next()) {
                String ssntam = rs.getString("ssn");

                // Truy vấn thứ hai
                String query1 = "SELECT phone FROM person_phone WHERE ssn=?";
                st1 = con.prepareStatement(query1);
                st1.setString(1, ssntam);
                rs1 = st1.executeQuery();

                if (rs1.next()) {
                    phoneField.setText("0" + rs1.getString("phone"));
                }

                // Set các giá trị còn lại
                getEmpID.setText(username);
                getSSN.setText(ssntam);
                nameField.setText(rs.getString("name"));
                joinDateField.setText(rs.getString("join_date"));
                managerIDField.setText(rs.getString("manager_id"));
                customerIDField1.setText(rs.getString("customer_id"));
                salaryField.setText(rs.getString("salary"));
                dobField.setText(rs.getString("dob"));
                streetField.setText(rs.getString("street"));
                cityField.setText(rs.getString("city"));
                provinceField.setText(rs.getString("province"));
            } else {
                JOptionPane.showMessageDialog(this, "Employee not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(employeeInfo.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (rs != null) {
                    rs.close();
                }
                if (st != null) {
                    st.close();
                }
                if (rs1 != null) {
                    rs1.close();
                }
                if (st1 != null) {
                    st1.close();
                }
            } catch (SQLException ex) {
                Logger.getLogger(employeeInfo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

