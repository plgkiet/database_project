    private boolean updateEmployeeInfo(String username, String street, String city, String province, String phone) {
        try {
            // Use a PreparedStatement to perform the first update
            String query1 = "UPDATE project.employee SET street=?, city=?, province=? WHERE employee_id=?";
            PreparedStatement st1 = con.prepareStatement(query1);
            st1.setString(1, street);
            st1.setString(2, city);
            st1.setString(3, province);
            st1.setString(4, username);

            // Execute the first update
            int rowsUpdated1 = st1.executeUpdate();

            // Use a separate PreparedStatement for the second update
            String query2 = "UPDATE project.person_phone SET phone=? WHERE ssn=?";
            PreparedStatement st2 = con.prepareStatement(query2);
            st2.setString(1, phone);
            st2.setString(2, getSSN.getText());

            // Execute the second update
            int rowsUpdated2 = st2.executeUpdate();

            // Check if both updates were successful
            return rowsUpdated1 > 0 && rowsUpdated2 > 0;
        } catch (SQLException ex) {
            Logger.getLogger(employeeInfo.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        }
    }

